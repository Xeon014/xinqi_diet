    @{ Label = "主食"; Key = "STAPLE"; Names = @("米饭","糙米饭","燕麦片","荞麦面","全麦面包","玉米","红薯","紫薯","土豆","山药","小米粥","黑米饭","藜麦","意面","通心粉","馒头","花卷","饺子皮","春卷皮","杂粮饭","年糕","米粉","河粉","乌冬面","荞麦饭") ; CalMin=80; CalStep=7; ProMin=2.0; ProStep=0.4; CarbMin=16; CarbStep=1.8; FatMin=0.2; FatStep=0.2 },
    @{ Label = "肉蛋奶"; Key = "PROTEIN"; Names = @("鸡胸肉","鸡腿肉","鸡蛋","鸭蛋","牛里脊","牛腱子","牛腩","猪里脊","猪后腿肉","虾仁","三文鱼","鳕鱼","金枪鱼","带鱼","豆腐","北豆腐","南豆腐","豆干","豆腐皮","低脂牛奶","酸奶","希腊酸奶","无糖豆浆","奶酪","鸡胸火腿") ; CalMin=90; CalStep=9; ProMin=8.0; ProStep=1.2; CarbMin=0.5; CarbStep=0.7; FatMin=1.0; FatStep=0.8 },
    @{ Label = "蔬果"; Key = "VEGETABLE_FRUIT"; Names = @("西兰花","菠菜","生菜","油麦菜","白菜","娃娃菜","空心菜","芹菜","西红柿","黄瓜","胡萝卜","白萝卜","南瓜","冬瓜","青椒","彩椒","茄子","蘑菇","香菇","金针菇","苹果","香蕉","橙子","梨","草莓") ; CalMin=18; CalStep=4; ProMin=0.6; ProStep=0.3; CarbMin=3.0; CarbStep=1.2; FatMin=0.1; FatStep=0.1 },
    @{ Label = "豆制品"; Key = "BEAN"; Names = @("黄豆","黑豆","红豆","绿豆","鹰嘴豆","芸豆","毛豆","纳豆","豆浆","豆乳","豆花","豆腐脑","豆腐丝","豆腐卷","腐竹","豆皮卷","豆蓉","豆沙","豆干条","豆丸","豆奶","扁豆","白芸豆","豌豆","青豆") ; CalMin=60; CalStep=6; ProMin=4.0; ProStep=0.8; CarbMin=6.0; CarbStep=1.6; FatMin=0.6; FatStep=0.4 },
    @{ Label = "饮品"; Key = "DRINK"; Names = @("黑咖啡","美式咖啡","拿铁","低脂拿铁","无糖绿茶","乌龙茶","红茶","柠檬水","苏打水","椰子水","番茄汁","苹果汁","橙汁","无糖可可","无糖酸梅汤","电解质饮料","低糖豆奶","燕麦奶","无糖气泡水","抹茶拿铁") ; CalMin=5; CalStep=8; ProMin=0.1; ProStep=0.3; CarbMin=0.5; CarbStep=1.4; FatMin=0.0; FatStep=0.2 },
