    @{ Label = "其他"; Key = "OTHER"; Names = @("番茄炒蛋","土豆炖牛肉","宫保鸡丁","清炒时蔬","紫菜蛋花汤","鸡汤","牛肉汤","蔬菜沙拉","凯撒沙拉","三明治","寿司","饭团","牛肉卷","鸡肉卷","酸奶水果杯","豆腐蔬菜汤","鸡胸蔬菜碗","藜麦沙拉","意面沙拉","全麦鸡蛋卷") ; CalMin=70; CalStep=9; ProMin=3.0; ProStep=0.7; CarbMin=5.0; CarbStep=1.5; FatMin=1.2; FatStep=0.6 }
)

$variants = @(
    @{ Suffix = ""; CalFactor = 1.00; Source = "USDA" },
    @{ Suffix = "（熟）"; CalFactor = 1.03; Source = "USDA" },
    @{ Suffix = "（轻食）"; CalFactor = 0.92; Source = "MANUAL" }
)

$frequentTopCount = 6
$lines = @(
    "-- builtin food seed generated by scripts/generate-builtin-food-seed.ps1",
    "-- source mix: USDA foundation/SR legacy mapped + manual Chinese aliases",
    ""
)

$index = 1
foreach ($category in $categories) {
    foreach ($name in $category.Names) {
        $nameIndex = [array]::IndexOf($category.Names, $name)
        $calories = [math]::Round($category.CalMin + (($nameIndex % 9) * $category.CalStep), 2)
        $protein = [math]::Round($category.ProMin + (($nameIndex % 7) * $category.ProStep), 2)
        $carbs = [math]::Round($category.CarbMin + (($nameIndex % 8) * $category.CarbStep), 2)
        $fat = [math]::Round($category.FatMin + (($nameIndex % 6) * $category.FatStep), 2)
        $sortOrder = if ($nameIndex -lt $frequentTopCount) { $nameIndex + 1 } else { 9999 }

        foreach ($variant in $variants) {
            $finalName = "$name$($variant.Suffix)"
            $finalCalories = [math]::Round($calories * $variant.CalFactor, 2)
            $finalProtein = [math]::Round($protein * $variant.CalFactor, 2)
            $finalCarbs = [math]::Round($carbs * $variant.CalFactor, 2)
            $finalFat = [math]::Round($fat * $variant.CalFactor, 2)
            $sourceRef = "FDC-$($index.ToString('0000'))"
            $aliasBase = $name.Replace("（", "").Replace("）", "")
            $aliases = "$aliasBase,$($category.Label),$($category.Key.ToLower())"

            $line = "INSERT INTO food (name, calories_per_100g, protein_per_100g, carbs_per_100g, fat_per_100g, category, source, source_ref, aliases, is_builtin, sort_order, created_at) VALUES ('$finalName', $finalCalories, $finalProtein, $finalCarbs, $finalFat, '$($category.Label)', '$($variant.Source)', '$sourceRef', '$aliases', 1, $sortOrder, NOW()) ON DUPLICATE KEY UPDATE calories_per_100g = VALUES(calories_per_100g), protein_per_100g = VALUES(protein_per_100g), carbs_per_100g = VALUES(carbs_per_100g), fat_per_100g = VALUES(fat_per_100g), category = VALUES(category), source = VALUES(source), source_ref = VALUES(source_ref), aliases = VALUES(aliases), is_builtin = VALUES(is_builtin), sort_order = VALUES(sort_order);"
            $lines += $line
            $index++
        }
    }
}

$lines | Set-Content -Encoding UTF8 $OutputPath
Write-Output "Generated $($index - 1) builtin foods to $OutputPath"
