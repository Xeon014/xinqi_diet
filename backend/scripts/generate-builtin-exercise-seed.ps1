$syncLines = @(
    "-- builtin exercise seed generated by scripts/generate-builtin-exercise-seed.ps1",
    "-- formula fields: met_value used with user weight and duration",
    ""
)

$idx = 1
foreach ($group in $catalog) {
    for ($i = 0; $i -lt $group.Items.Count; $i++) {
        $item = $group.Items[$i]
        $met = [math]::Round($group.BaseMet + (($i % 6) * $group.Step), 2)
        $sortOrder = if ($i -lt 6) { $i + 1 } else { 9999 }

        foreach ($variant in $variants) {
            $finalName = "$($item.Zh)$($variant.NameSuffix)"
            $finalMet = [math]::Round($met * $variant.Factor, 2)
            $sourceRef = "EX-$($idx.ToString('0000'))"
            $englishAlias = "$($item.En)$($variant.AliasSuffix)"
            $aliases = @(
                $finalName,
                $item.Zh,
                $item.En,
                $englishAlias,
                $group.Category,
                "exercise"
            ) | Where-Object { $_ } | Select-Object -Unique
            $aliasesText = Escape-SqlValue ($aliases -join ",")
            $nameSql = Escape-SqlValue $finalName

            $syncLines += "INSERT INTO exercise (name, met_value, category, source, source_ref, aliases, is_builtin, sort_order, created_at) VALUES ('$nameSql', $finalMet, '$($group.Category)', '$($variant.Source)', '$sourceRef', '$aliasesText', 1, $sortOrder, NOW()) ON DUPLICATE KEY UPDATE met_value = VALUES(met_value), category = VALUES(category), source = VALUES(source), source_ref = VALUES(source_ref), aliases = VALUES(aliases), is_builtin = VALUES(is_builtin), sort_order = VALUES(sort_order);"
            $idx++
        }
    }
}

$bootstrapLines = @(
    "-- builtin exercise bootstrap generated by scripts/generate-builtin-exercise-seed.ps1",
    "DELETE FROM exercise WHERE is_builtin = 1;",
    ""
) + $syncLines

