$variants = @(
    @{ NameSuffix = ""; AliasSuffix = ""; Factor = 1.0; Source = "USDA" },
    @{ NameSuffix = "（中等）"; AliasSuffix = " (Medium)"; Factor = 1.12; Source = "MANUAL" }
